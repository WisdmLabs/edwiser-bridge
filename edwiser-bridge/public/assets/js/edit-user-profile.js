	function wdmShowCountries(countryElementId, stateElementId)
	{
		// given the id of the <select> tag as function argument, it inserts <option> tags
		var countryElement           = document.getElementById(countryElementId);
		countryElement.length        =0;
		countryElement.options[0]    = new Option(ebEditProfile.default, '0');
		countryElement.selectedIndex = 0;
		for (var i=0; i<allCountries.length; i++) {
			countryElement.options[countryElement.length] = new Option(allCountries[i].n, allCountries[i].i.toUpperCase());
		}
	}


